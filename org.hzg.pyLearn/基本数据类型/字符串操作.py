#字符串拼接
print("hello " + "world")
#字符串截取
print("woaini"[0])   #正向下标
print("woaini"[-1])  #反向下标 -1代表逆向第一个
print("woaini"[0:2])
print("woaini"[2:4])
print("woaini"[4:6])

print("hello world"[6:11])
print("hello world"[6:])
print("hello world"[:-5])
print("hello world"[:-5])  #表示从头开始，一直到倒数第五个的位置
print("hello world"[-5:])  #表示从倒数第五个的位置一直到字符串结束

#重复多次
print("hello world" * 0)

