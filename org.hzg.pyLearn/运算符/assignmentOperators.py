# 赋值运算符
num_a = 5
num_b = 8
print(num_a)
num_b += num_a   #表示numb = numb + numa
print(num_b)
num_b -= num_a   #表示numb = numb - numa
print(num_b)
num_b *= num_a   #表示numb = numb * numa
print(num_b)
num_b /= num_a   #表示numb = numb / numa
print(num_b)
num_b %= num_a   #表示numb = numb % numa
print(num_b)
num_b //= num_a  #表示numb = numb // numa
print(num_b)
num_b **= num_a  #表示numb = numb ** numa
print(num_b)
